- (void)viewDidLoad
{
    [super viewDidLoad];

    self.navigationController.navigationBar.barStyle = UIBarStyleBlackOpaque;
    
    appDelegate = (iRankAppDelegate *)[[UIApplication sharedApplication] delegate];
    [appDelegate showLoadingView:@"carregando lista de rankings"];
